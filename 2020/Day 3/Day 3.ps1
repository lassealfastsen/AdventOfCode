$stepSize = 3
$currentPos = 0
$trees = 0
foreach ($line in $hillside) {
    $currentPos += $stepSize
    if ((Get-Char $line $currentPos) -eq '#') {
        $trees++
    }
}



#Part 2 convert to for loop to allow multiple steps down at a time.
$slopeMap = @(
    [PSCustomObject]@{right = 1; down = 1; trees=0},
    [PSCustomObject]@{right = 3; down = 1; trees=0},
    [PSCustomObject]@{right = 5; down = 1; trees=0},
    [PSCustomObject]@{right = 7; down = 1; trees=0},
    [PSCustomObject]@{right = 1; down = 2; trees=0}
)

$result = 1
foreach ($map in $slopeMap) {
    $currentPos = 0
    #$trees = 0
    for ($i = 0; $i -lt $hillside.Count; $i+=$map.down) {
        
        #Adjust first line to be parsed if downstep is larger than 1
        #Could be prettier!!
        if (($map.down -gt 1) -and ($i -eq 0)) {
            $i+=$map.down-1
        }

        $currentPos += $map.right
        if ((Get-Char $hillside[$i] $currentPos) -eq '#') {
            $map.trees++
        }
    }
    #if ($result -eq 0) {$result = $map.trees} else {$result = $result * $map.trees}
    $result = $result * $map.trees
}







