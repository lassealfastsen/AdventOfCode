Function Get-Char {
    Param(
        [Parameter(Mandatory=$true, Position = 1)][String]$str,
        [Parameter(Mandatory=$true, Position = 2)][int]$position
    )

    $strLength = $str.Length

    $PosInStr = ($position %  $strLength)

    return $str.ToCharArray()[$PosInStr]
}



